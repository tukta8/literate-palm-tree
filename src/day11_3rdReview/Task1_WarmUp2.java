package day11_3rdReview;

import java.util.Scanner;

public class Task1_WarmUp2 {

	public static void main(String[] args) {
/*
- Write program to print all number in reverse in one line (from `n` to 1)
 - using while loop
*/
		

		Scanner scan = new Scanner(System.in);
		System.out.println(" Enter the number");
		int a = scan.nextInt();

		while ( a >=1) {
			System.out.println("The number = " + a);
			a--;
		}
		scan.close();
	
		
		
		
	}}	
		
		
