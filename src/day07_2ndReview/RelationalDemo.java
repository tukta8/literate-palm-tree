package day07_2ndReview;

public class RelationalDemo {

	public static void main(String[] args) {
		// Relational Operator 
		/*
		  1. >
		  2. <
		  3. >=
		  4. <=
		  5. == mean equal
		  6. != mean not equal
		 */
		System.out.println(10!=4); // true
		
		// ==, != use to compare primitive data type
		char ch1 = 'a';
		char ch2 = 'b';
		System.out.println(ch1 == ch2);// false
		
	System.out.println();
		
		
		
		
		
		
		
		
		
		

	}

}
